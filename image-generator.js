// Enhanced image generation detection
function detectImageRequest(text) {
  const imagePatterns = [
    /generate\\s+image/i,
    /create\\s+image/i,
    /make\\s+image/i,
    /draw\\s+image/i,
    /create\\s+a?\\s*design/i,
    /make\\s+a?\\s*design/i,
    /generate\\s+design/i,
    /design\\s*:/i,
    /create\\s+artwork/i,
    /make\\s+artwork/i,
    /generate\\s+art/i,
    /create\\s+art/i
  ];
  
  return imagePatterns.some(pattern => pattern.test(text));
}

// Process message for image generation
function processMessageForImages(message) {
  if (detectImageRequest(message)) {
    console.log('ðŸŽ¨ IMAGE GENERATION REQUEST DETECTED:', message);
    return true;
  }
  
  return false;
}

